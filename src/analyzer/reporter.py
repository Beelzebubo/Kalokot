    def generate_heatmap(self, report: RiskReport) -> str:
        """Generate a compact section-by-section risk heatmap.

        Terminal-friendly; uses emoji bars for colour.

        Args:
            report: The RiskReport to render.

        Returns:
            A multi-line string with the heatmap.
        """
        lines = []
        lines.append("")
        lines.append("  RISK HEATMAP")
        lines.append("  " + "─" * 40)

        sections_in_order = [
            "details",
            "specification",
            "budget",
            "timeline",
            "evaluation_criteria",
            "terms_and_conditions",
        ]
        for section in sections_in_order:
            score = report.section_scores.get(section, RiskLevel.GREEN)
            name = section.replace("_", " ").title()
            bar = self._risk_bar(score)
            lines.append(f"  {bar} {name}")

        lines.append("  " + "─" * 40)
        overall = report.overall_risk
        bar = self._risk_bar(overall)
        lines.append(f"  {bar} OVERALL: {overall.upper()}")
        lines.append("")
        return "\n".join(lines)

    # ── Formatting helpers ──────────────────────────────────────────

    def _format_risk(self, level: RiskLevel) -> str:
        """Format a risk level with an emoji prefix."""
        icon = self._risk_emoji(level)
        return f"{icon} {level.upper()}"

    def _risk_emoji(self, level: RiskLevel) -> str:
        """Return a coloured-circle emoji for a risk level."""
        return {"green": "🟢", "yellow": "🟡", "red": "🔴"}.get(
            level.value, "⚪"
        )

    def _severity_icon(self, severity: Severity) -> str:
        """Return an icon emoji for a severity level."""
        return {
            "low": "ℹ️",
            "medium": "⚠️",
            "high": "🚨",
            "critical": "💀",
        }.get(severity.value, "⚪")

    # Helper to clean excerpt text for readability
    def _clean_excerpt(self, text: str) -> str:
        """Sanitize excerpt text: keep alphanumerics, Devanagari, basic punctuation.
        Collapse whitespace and filter out obvious OCR noise.
        """
        cleaned = re.sub(r'[^A-Za-z0-9\u0900-\u097F\s.,;:!?\-\'\"]+', ' ', text)
        cleaned = re.sub(r'\s+', ' ', cleaned).strip()
        # Discard very short or empty excerpts – they are usually garbled PDF noise
        if len(cleaned) < 20:
            return ''
        # Discard common PDF page markers (e.g., "--- Page 1 ---")
        if cleaned.lower().startswith('--- page'):
            return ''
        # Tokenize for further heuristics
        tokens = cleaned.split()
        if not tokens:
            return ''
        # Existing average‑token‑length heuristic (still useful)
        avg_len = sum(len(tok) for tok in tokens) / len(tokens)
        if avg_len < 3.0:
            return ''
        # ---- New noise detection heuristics ----
        # Ratio of non‑alphanumeric characters (punctuation, symbols)
        non_alpha_ratio = sum(1 for c in cleaned if not c.isalnum()) / len(cleaned)
        # Count tokens that contain at least one alphabetic character (Latin or Devanagari)
        alpha_token_count = sum(1 for t in tokens if re.search(r'[A-Za-z\u0900-\u097F]', t))
        # Ratio of single‑character tokens (often OCR artefacts)
        single_char_ratio = sum(1 for t in tokens if len(t) == 1) / len(tokens)
        # If any of the following indicate noise, discard the excerpt
        if (
            non_alpha_ratio > 0.4 or
            alpha_token_count < 3 or
            single_char_ratio > 0.5 or
            (len(tokens) > 30 and (non_alpha_ratio > 0.2 or single_char_ratio > 0.3))
        ):
            return ''
        return cleaned

    def _risk_color(self, level: RiskLevel) -> str:
        """Return a CSS hex colour for a risk level."""
        return {"green": "#22c55e", "yellow": "#eab308", "red": "#ef4444"}.get(
            level.value, "#999"
        )

    def _severity_color(self, severity: Severity) -> str:
        """Return a CSS hex colour for a severity level."""
        return {
            "low": "#6b7280",
            "medium": "#eab308",
            "high": "#f97316",
            "critical": "#ef4444",
        }.get(severity.value, "#999")

    def _risk_bar(self, level: RiskLevel) -> str:
        """Return a coloured-circle emoji (alias for _risk_emoji)."""
        return self._risk_emoji(level)
