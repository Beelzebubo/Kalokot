        lines.append("-" * 60)
        lines.append("SECTION RISK SCORES:")
        lines.append("-" * 60)
        for section, score in report.section_scores.items():
            icon = self._risk_emoji(score)
            name = section.replace("_", " ").title()
            lines.append(f"  {icon} {name}: {score.upper()}")
        lines.append("")

        # ── Flagged clauses ─────────────────────────────────────────
        if report.flagged_clauses:
            lines.append("-" * 60)
            lines.append("FLAGGED CLAUSES / RED FLAGS:")
            lines.append("-" * 60)
            for i, flag in enumerate(report.flagged_clauses, 1):
                severity_icon = self._severity_icon(flag.severity)
                lines.append("")
                lines.append(
                    f"  #{i} {severity_icon} "
                    f"[{flag.severity.upper()}] {flag.label}"
                )
                lines.append(f"     {flag.description}")
                if flag.law_reference:
                    lines.append(f"     Law: {flag.law_reference}")
                lines.append(f"     Location: {flag.location}")
                lines.append(f"     Suggestion: {flag.suggestion}")
                if flag.excerpt:
                    cleaned = self._clean_excerpt(flag.excerpt)
                    if cleaned:
                        excerpt = cleaned[:150] + ("..." if len(cleaned) > 150 else "")
                        lines.append(f'     Excerpt: "{excerpt}"')
        else:
            lines.append("No red flags detected.")

        lines.append("")
        lines.append("=" * 60)
        lines.append(
            "Report generated by OpenTender + Counsel"
            " — AI-assisted analysis"
        )
        lines.append("=" * 60)

        return "\n".join(lines)

